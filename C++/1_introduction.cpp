#include <iostream>  // Includes the iostream header file,
                     // iostream stands for standard input-output stream
                     // Used for input and output functionality
using namespace std; // This allows us to use names like cin / cout
                     // without specifying std:: before them.

int main() {
  std::cout << "Hello world" << endl; // if we dont use "namespace std"

  cout << "Hello World"; // when we use "namespace std".

  return 0;
}

